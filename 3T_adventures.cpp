void obslugaWejscia() {
  if (!(PIND & B10000000)) {
    dotY -= movement_speed;
    if (dotY < 6) dotY = 6; // Ograniczenie dla paska zdrowia
    aktualny_indeks = 2;
  }
  if (!(PINB & B00000010)) {
    dotY += movement_speed;
    if (dotY + BMP_HEIGHT > SCREEN_HEIGHT) dotY = SCREEN_HEIGHT - BMP_HEIGHT;
    aktualny_indeks = 3;
  }
  if (!(PINB & B00000001)) {
    dotX += movement_speed;
    if (dotX + BMP_WIDTH > SCREEN_WIDTH) dotX = SCREEN_WIDTH - BMP_WIDTH;
    aktualny_indeks = 0;
  }
  if (!(PINB & B00000100)) {
    dotX -= movement_speed;
    if (dotX < 0) dotX = 0;
    aktualny_indeks = 1;
  }
}
void aktualizujRekina() {
  if (rekinZlapany) return; 
  static int opoznienieRuchu = 0;
  opoznienieRuchu++;
  if(opoznienieRuchu < 3) return; 
  opoznienieRuchu = 0;
  
  int diffX = dotX - rekinX;
  int diffY = dotY - rekinY;

  if (abs(diffX) > abs(diffY)) {
    if (diffX > 0) {
      rekinX += predkoscRekina;
      rekinIndeks = 2; 
    } else if (diffX < 0) {
      rekinX -= predkoscRekina;
      rekinIndeks = 3; 
    }
  } else {
    if (diffY > 0) {
      rekinY += predkoscRekina;
      rekinIndeks = 1; 
    } else if (diffY < 0) {
      rekinY -= predkoscRekina;
      rekinIndeks = 0; 
    }
  }
  if (rekinY < 6) rekinY = 6;
}
void rysujRekina(unsigned long teraz) {
  if (atakTrafiony && (teraz - atakTrafionyCzas >= CZAS_ANIMACJI_ATAKU)) {
    atakTrafiony = false;
  }
  if (atakTrafiony) {
    if ((teraz / 80) % 2 == 0) {
      display.drawBitmap(
        rekinX, rekinY,
        rekiny[rekinIndeks].bitmapa,
        rekiny[rekinIndeks].szerokosc,
        rekiny[rekinIndeks].wysokosc,
        SH110X_WHITE
      );
    }
    display.drawBitmap(rekinX + 8, rekinY + 5, atak, 16, 16, SH110X_WHITE);
  }

  if (rekinZlapany) {
    display.drawBitmap(
      rekinX, rekinY,
      rekin_odwrocony, 
      REKIN_SIZE, REKIN_SIZE,
      SH110X_WHITE
    );
  } 
  else {
    display.drawBitmap(
      rekinX, rekinY,
      rekiny[rekinIndeks].bitmapa,
      rekiny[rekinIndeks].szerokosc,
      rekiny[rekinIndeks].wysokosc,
      SH110X_WHITE
    );
  }
}
